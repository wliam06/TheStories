import UIKit

extension UIColor {
    open class var liteGrey: UIColor {
        return UIColor(red: 223.0 / 255.0, green: 230.0 / 255.0, blue: 233.0 / 255.0, alpha: 1)
    }

    open class var whiteGray: UIColor {
        return UIColor(red: 236.0 / 255.0, green: 240.0 / 255.0, blue: 241.0 / 255.0, alpha: 1)
    }

    open class var lightBlue: UIColor {
        return UIColor(red: 0, green: 0.478431, blue: 1, alpha: 1)
    }
}
